# Print all the odd numbers from 0 to 100 (using a while loop)
# Author: Israfil Palabay
# Date: March 13, 2025

# Initialize
num = 1 # Start from 1 since it is the first odd number

# While loop
while num <= 100:
    print(num) # Print the odd numbers
    num += 2 # Increment by 2 to always get the next odd number

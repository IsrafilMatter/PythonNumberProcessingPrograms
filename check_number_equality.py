# Ask the user to input 2 numbers
first_number = float(input("Enter the first number: "))
second_number = float(input("Enter the second number: "))

# Comparing the 2 numbers if it is Equal
if first_number == second_number:
    print('Equal')
else:
    print('Not Equal')